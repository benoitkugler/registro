          Nous croyons que Dieu a créé l’homme et la femme à son image. D’abord,
          Dieu a créé Adam, puis Ève à partir de l’homme, pour qu’elle soit son
          aide. Après que le premier couple a désobéi, toute la race humaine est
          tombée dans l’esclavage du péché, perdant sa relation avec Dieu. Elle
          se retrouve ainsi sous la condamnation d’un châtiment éternel dont
          elle ne peut se racheter par ses propres efforts ou mérites.
          <h3>Article 7 – Le salut</h3>
          Nous croyons au salut par la seule grâce de Dieu. Par sa Parole et son
          Esprit, Dieu appelle les hommes à être réconciliés avec lui et donne à
          l’Église le mandat d’annoncer l’Évangile et de faire des disciples.
